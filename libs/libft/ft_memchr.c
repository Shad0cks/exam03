#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*p;
	size_t			i;

	i = 0;
	p = (unsigned char *)s;
	while (i < n)
	{
		if ((unsigned char)c == p[i])
			return (p + i);
		i++;
	}
	return (NULL);
}
